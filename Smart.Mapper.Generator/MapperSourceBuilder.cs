            builder.Indent().Append(mapCollection.Mapper!).Append("(").Append(srcExpr).Append("[__i], __dest);").NewLine();
            builder.Indent().Append(containerExpr).Append(".Add(__dest);").NewLine();
        }
        builder.EndScope();
    }


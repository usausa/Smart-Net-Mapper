    internal static void BuildPropertyAssignment(SourceBuilder builder, PropertyMappingModel mapping, string sourceParamName, string destVarName, MapperMethodModel method, bool nullChecked = false)
    {
        if (mapping.HasCondition())
        {
            var sourceAccessor = BuildSourceAccessor(mapping.SourcePath, sourceParamName, nullChecked);
            builder.Indent().Append("if (").Append(mapping.ConditionMethod!).Append("(").Append(sourceAccessor);
            if (mapping.ConditionAcceptsCustomParameters)
            {
                foreach (var customParam in method.CustomParameters)
                {
                    builder.Append(", ").Append(customParam.Name);
                }
            }
            builder.Append("))").NewLine();
            builder.BeginScope();
        }

        if (mapping.IsSourceNullable && !mapping.HasConverter() && !mapping.RequiresConversion &&
            mapping.NullBehavior == NullBehaviorType.Skip)
        {
            BuildSkipAssignment(builder, mapping, sourceParamName, destVarName, nullChecked);
        }
        else if (mapping.IsSourceNullable && mapping.RequiresConversion && !mapping.HasConverter())
        {
            BuildNullableSourceConversion(builder, mapping, sourceParamName, destVarName, method, nullChecked);
        }
        else
        {
            BuildStandardAssignment(builder, mapping, sourceParamName, destVarName, method, nullChecked);
        }

        if (mapping.HasCondition())
        {
            builder.EndScope();
        }
    }

    // Emits a guarded assignment for NullBehavior.Skip when no type conversion is required:
    // a nullable source is copied only when it has a value, otherwise the destination is left as-is.
    internal static void BuildSkipAssignment(SourceBuilder builder, PropertyMappingModel mapping, string sourceParamName, string destVarName, bool nullChecked)
    {
        var sourceAccessor = BuildSourceAccessor(mapping.SourcePath, sourceParamName, nullChecked);
        var isNullableValueType = mapping.SourceType.Contains("?") || mapping.SourceType.Contains("Nullable<");

        builder.Indent().Append("if (").Append(sourceAccessor).Append(" is not null)").NewLine();
        builder.BeginScope();
        builder.Indent().Append(destVarName).Append(".").Append(mapping.TargetPath).Append(" = ").Append(sourceAccessor);
        if (!mapping.IsTargetNullable)
        {
            builder.Append(isNullableValueType ? ".GetValueOrDefault()" : "!");
        }
        builder.Append(";").NewLine();
        builder.EndScope();
    }

    internal static void BuildStandardAssignment(SourceBuilder builder, PropertyMappingModel mapping, string sourceParamName, string destVarName, MapperMethodModel method, bool nullChecked)
    {
        builder.Indent();
        builder.Append(destVarName).Append(".").Append(mapping.TargetPath).Append(" = ");

        if (mapping.HasConverter())
        {
            var sourceAccessor = BuildSourceAccessor(mapping.SourcePath, sourceParamName, nullChecked);
            builder.Append(mapping.ConverterMethod!).Append("(").Append(sourceAccessor);

            if (mapping.ConverterAcceptsCustomParameters)
            {
                foreach (var customParam in method.CustomParameters)
                {
                    builder.Append(", ").Append(customParam.Name);
                }
            }

            builder.Append(")");
        }
        else if (mapping.RequiresConversion)
        {
            BuildTypeConversion(builder, mapping, sourceParamName, nullChecked, method);
        }
        else
        {
            var sourceAccessor = BuildSourceAccessor(mapping.SourcePath, sourceParamName, nullChecked);
            builder.Append(sourceAccessor);

            if (mapping.HasNullSubstitute() && mapping.IsSourceNullable)
            {
                builder.Append(" ?? ").Append(mapping.NullSubstitute!);
            }
            else if (mapping.RequiresNullCoalescing())
            {
                // A nullable value type source assigned to a non-nullable target must be unwrapped;
                // the null-forgiving operator alone keeps the type nullable (CS0266). Nullable reference
                // types display without "?" in FullyQualifiedFormat, so they correctly keep using "!".
                if (mapping.SourceType.Contains("?") || mapping.SourceType.Contains("Nullable<"))
                {
                    builder.Append(".GetValueOrDefault()");
                }
                else
                {
                    builder.Append("!");
                }
            }
        }

        builder.Append(";").NewLine();
    }

    internal static void BuildNullableSourceConversion(SourceBuilder builder, PropertyMappingModel mapping, string sourceParamName, string destVarName, MapperMethodModel method, bool nullChecked)
    {
        var sourceAccessor = BuildSourceAccessor(mapping.SourcePath, sourceParamName, nullChecked);

        var isNullableValueType = mapping.SourceType.Contains("?") ||
                                   mapping.SourceType.Contains("Nullable<");

        if (mapping.NullBehavior == NullBehaviorType.Skip)
        {
            builder.Indent().Append("if (").Append(sourceAccessor).Append(" is not null)").NewLine();
            builder.BeginScope();
            builder.Indent();
            builder.Append(destVarName).Append(".").Append(mapping.TargetPath).Append(" = ");

            if (isNullableValueType)
            {
                BuildTypeConversionWithValueAccess(builder, mapping, sourceAccessor, method);
            }
            else
            {
                BuildTypeConversion(builder, mapping, sourceParamName, nullChecked, method);
            }

            builder.Append(";").NewLine();
            builder.EndScope();
        }
        else if (mapping.HasNullSubstitute())
        {
            builder.Indent();
            builder.Append(destVarName).Append(".").Append(mapping.TargetPath).Append(" = ");
            builder.Append(sourceAccessor).Append(" is not null ? ");

            if (isNullableValueType)
            {
                BuildTypeConversionWithValueAccess(builder, mapping, sourceAccessor, method);
            }
            else
            {
                BuildTypeConversion(builder, mapping, sourceParamName, nullChecked, method);
            }

            builder.Append(" : ").Append(mapping.NullSubstitute!).Append(";").NewLine();
        }
        else
        {
            if (mapping.IsTargetNullable)
            {
                builder.Indent();
                builder.Append(destVarName).Append(".").Append(mapping.TargetPath).Append(" = ");
                builder.Append(sourceAccessor).Append(" is not null ? ");

                if (isNullableValueType)
                {
                    BuildTypeConversionWithValueAccess(builder, mapping, sourceAccessor, method);
                }
                else
                {
                    BuildTypeConversion(builder, mapping, sourceParamName, nullChecked, method);
                }

                builder.Append(" : null;").NewLine();
            }
            else
            {
                builder.Indent();
                builder.Append(destVarName).Append(".").Append(mapping.TargetPath).Append(" = ");
                builder.Append(sourceAccessor).Append(" is not null ? ");

                if (isNullableValueType)
                {
                    BuildTypeConversionWithValueAccess(builder, mapping, sourceAccessor, method);
                }
                else
                {
                    BuildTypeConversion(builder, mapping, sourceParamName, nullChecked, method);
                }

                builder.Append(" : default!;").NewLine();
            }
        }
    }

    internal static void BuildTypeConversionWithValueAccess(SourceBuilder builder, PropertyMappingModel mapping, string sourceAccessor, MapperMethodModel method)
    {
        if (mapping.IsEnumMapping())
        {
            // Guarded by the surrounding `is not null` check, so GetValueOrDefault skips the
            // redundant HasValue re-check (and throw path) that .Value would perform.
            var valueAccessor = sourceAccessor + ".GetValueOrDefault()";
            var enumMapping = new PropertyMappingModel
            {
                EnumMappingKind = mapping.EnumMappingKind,
                SourceUnderlyingType = mapping.SourceUnderlyingType,
                TargetUnderlyingType = mapping.TargetUnderlyingType,
                SourceType = mapping.SourceType,
                TargetType = mapping.TargetType,
                SourceEnumMembers = mapping.SourceEnumMembers,
                DestEnumMembers = mapping.DestEnumMembers
            };
            BuildEnumConversion(builder, enumMapping, valueAccessor);
            return;
        }

        var converterTypeName = method.MapConverterTypeName ?? MapperModelBuilder.DefaultValueConverterTypeName;

        if (mapping.HasSpecializedConverter())
        {
            if (mapping.HasCulture())
            {
                var formatArg = DetermineFormatArg(mapping);
                builder.Append(converterTypeName)
                       .Append(".")
                       .Append(mapping.SpecializedConverterMethod!)
                       .Append("(")
                       .Append(sourceAccessor)
                       .Append(".GetValueOrDefault(), ")
                       .Append(GetCultureFieldName(mapping.EffectiveCulture!))
                       .Append(formatArg)
                       .Append(")");
            }
            else
            {
                builder.Append(converterTypeName)
                       .Append(".")
                       .Append(mapping.SpecializedConverterMethod!)
                       .Append("(")
                       .Append(sourceAccessor)
                       .Append(".GetValueOrDefault())");
            }
        }
        else if (mapping.HasParsableMethod())
        {
            var targetTypeForParse = !String.IsNullOrEmpty(mapping.TargetUnderlyingType) ? mapping.TargetUnderlyingType : mapping.TargetType;
            var cultureArg = mapping.HasCulture()
                ? GetCultureFieldName(mapping.EffectiveCulture!)
                : "global::System.Globalization.CultureInfo.InvariantCulture";
            if (mapping.ParseMethod == ParseMethodKind.SpanParsable)
            {
                builder.Append(targetTypeForParse).Append(".Parse(global::System.MemoryExtensions.AsSpan(")
                       .Append(sourceAccessor).Append(".GetValueOrDefault()), ")
                       .Append(cultureArg).Append(")");
            }
            else
            {
                builder.Append(targetTypeForParse).Append(".Parse(")
                       .Append(sourceAccessor).Append(".GetValueOrDefault(), ")
                       .Append(cultureArg).Append(")");
            }
        }
        else if (mapping.RequiresExplicitNumericCast)
        {
            var targetTypeForCast = !String.IsNullOrEmpty(mapping.TargetUnderlyingType) ? mapping.TargetUnderlyingType : mapping.TargetType;
            builder.Append("(").Append(targetTypeForCast).Append(")").Append(sourceAccessor).Append(".GetValueOrDefault()");
        }
        else if (mapping.UserDefinedConversion == UserDefinedConversionKind.Implicit)
        {
            builder.Append(sourceAccessor).Append(".GetValueOrDefault()");
        }
        else if (mapping.HasUserDefinedExplicit())
        {
            var targetTypeForCast = !String.IsNullOrEmpty(mapping.TargetUnderlyingType) ? mapping.TargetUnderlyingType : mapping.TargetType;
            builder.Append("(").Append(targetTypeForCast).Append(")").Append(sourceAccessor).Append(".GetValueOrDefault()");
        }
        else if (mapping.UseFormattable)
        {
            var formatArg = DetermineFormatArg(mapping);
            var cultureArg = mapping.HasCulture()
                ? GetCultureFieldName(mapping.EffectiveCulture!)
                : "global::System.Globalization.CultureInfo.InvariantCulture";
            var formatStr = formatArg.Length > 2 ? formatArg.Substring(2) : "null";
            builder.Append(sourceAccessor).Append(".GetValueOrDefault().ToString(").Append(formatStr).Append(", ").Append(cultureArg).Append(")");
        }
        else
        {
            var converterMethodName = method.MapConverterMethodName;
            var sourceTypeForConversion = !String.IsNullOrEmpty(mapping.SourceUnderlyingType) ? mapping.SourceUnderlyingType : mapping.SourceType;
            var targetTypeForConversion = !String.IsNullOrEmpty(mapping.TargetUnderlyingType) ? mapping.TargetUnderlyingType : mapping.TargetType;

            builder.Append(converterTypeName)
                   .Append(".").Append(converterMethodName).Append("<")
                   .Append(sourceTypeForConversion)
                   .Append(", ")
                   .Append(targetTypeForConversion)
                   .Append(">(")
                   .Append(sourceAccessor)
                   .Append(".GetValueOrDefault())");
        }
    }

    internal static string GetNullCheckCondition(PropertyMappingModel mapping, string sourceParamName)
    {
        var conditions = new List<string>();

        var sourceSegmentsArr = mapping.SourcePathSegments;
        if (sourceSegmentsArr.Count > 0)
        {
            var pathBuilder = new StringBuilder();
            pathBuilder.Append(sourceParamName);

            foreach (var segment in sourceSegmentsArr)
            {
                pathBuilder.Append('.').Append(segment.Path.Split('.').Last());
                if (segment.IsNullable)
                {
                    conditions.Add($"{pathBuilder} is not null");
                }
            }
        }

        return String.Join(" && ", conditions);
    }

    internal static string BuildSourceAccessor(string sourcePath, string sourceParamName, bool nullChecked = false)
    {
        if (!sourcePath.Contains('.'))
        {
            return $"{sourceParamName}.{sourcePath}";
        }

        var parts = sourcePath.Split('.');
        var result = new StringBuilder();
        result.Append(sourceParamName);

        for (var i = 0; i < parts.Length; i++)
        {
            result.Append('.');
            result.Append(parts[i]);

            if ((i < parts.Length - 1) && (!nullChecked))
            {
                result.Append('!');
            }
        }

        return result.ToString();
    }

    internal static void BuildTypeConversion(SourceBuilder builder, PropertyMappingModel mapping, string sourceParamName, bool nullChecked, MapperMethodModel method)
    {
        var sourceExpr = BuildSourceAccessor(mapping.SourcePath, sourceParamName, nullChecked);

        if (mapping.IsEnumMapping())
        {
            BuildEnumConversion(builder, mapping, sourceExpr);
            return;
        }

        var converterTypeName = method.MapConverterTypeName ?? MapperModelBuilder.DefaultValueConverterTypeName;

        if (mapping.HasSpecializedConverter())
        {
            if (mapping.HasCulture())
            {
                var formatArg = DetermineFormatArg(mapping);
                builder.Append(converterTypeName)
                       .Append(".")
                       .Append(mapping.SpecializedConverterMethod!)
                       .Append("(")
                       .Append(sourceExpr)
                       .Append(", ")
                       .Append(GetCultureFieldName(mapping.EffectiveCulture!))
                       .Append(formatArg)
                       .Append(")");
            }
            else
            {
                builder.Append(converterTypeName)
                       .Append(".")
                       .Append(mapping.SpecializedConverterMethod!)
                       .Append("(")
                       .Append(sourceExpr)
                       .Append(")");
            }
        }
        else if (mapping.HasParsableMethod())
        {
            var targetTypeForParse = !String.IsNullOrEmpty(mapping.TargetUnderlyingType) ? mapping.TargetUnderlyingType : mapping.TargetType;
            var cultureArg = mapping.HasCulture()
                ? GetCultureFieldName(mapping.EffectiveCulture!)
                : "global::System.Globalization.CultureInfo.InvariantCulture";
            if (mapping.ParseMethod == ParseMethodKind.SpanParsable)
            {
                builder.Append(targetTypeForParse).Append(".Parse(global::System.MemoryExtensions.AsSpan(")
                       .Append(sourceExpr).Append("), ")
                       .Append(cultureArg).Append(")");
            }
            else
            {
                builder.Append(targetTypeForParse).Append(".Parse(")
                       .Append(sourceExpr).Append(", ")
                       .Append(cultureArg).Append(")");
            }
        }
        else if (mapping.RequiresExplicitNumericCast)
        {
            var targetTypeForCast = !String.IsNullOrEmpty(mapping.TargetUnderlyingType) ? mapping.TargetUnderlyingType : mapping.TargetType;
            builder.Append("(").Append(targetTypeForCast).Append(")").Append(sourceExpr);
        }
        else if (mapping.HasUserDefinedExplicit())
        {
            var targetTypeForCast = !String.IsNullOrEmpty(mapping.TargetUnderlyingType) ? mapping.TargetUnderlyingType : mapping.TargetType;
            builder.Append("(").Append(targetTypeForCast).Append(")").Append(sourceExpr);
        }
        else if (mapping.UseFormattable)
        {
            var formatArg = DetermineFormatArg(mapping);
            var cultureArg = mapping.HasCulture()
                ? GetCultureFieldName(mapping.EffectiveCulture!)
                : "global::System.Globalization.CultureInfo.InvariantCulture";
            var formatStr = formatArg.Length > 2 ? formatArg.Substring(2) : "null";
            builder.Append(sourceExpr).Append(".ToString(").Append(formatStr).Append(", ").Append(cultureArg).Append(")");
        }
        else
        {
            var converterMethodName = method.MapConverterMethodName;
            var sourceTypeForConversion = !String.IsNullOrEmpty(mapping.SourceUnderlyingType) ? mapping.SourceUnderlyingType : mapping.SourceType;
            var targetTypeForConversion = !String.IsNullOrEmpty(mapping.TargetUnderlyingType) ? mapping.TargetUnderlyingType : mapping.TargetType;

            builder.Append(converterTypeName)
                   .Append(".").Append(converterMethodName).Append("<")
                   .Append(sourceTypeForConversion)
                   .Append(", ")
                   .Append(targetTypeForConversion)
                   .Append(">(")
                   .Append(sourceExpr)
                   .Append(")");
        }
    }

    internal static string DetermineFormatArg(PropertyMappingModel mapping)
    {
        var isDateTimeFamily = TypeNameHelper.IsDateTimeType(mapping.SourceUnderlyingType) || TypeNameHelper.IsDateTimeType(mapping.TargetUnderlyingType);
        var format = isDateTimeFamily ? mapping.EffectiveDateTimeFormat : mapping.EffectiveNumberFormat;
        return format is null ? ", null" : $", \"{format}\"";
    }

    internal static void BuildEnumConversion(SourceBuilder builder, PropertyMappingModel mapping, string sourceExpr)
    {
        var targetType = !String.IsNullOrEmpty(mapping.TargetUnderlyingType) ? mapping.TargetUnderlyingType : mapping.TargetType;

        switch (mapping.EnumMappingKind)
        {
            case EnumMappingKind.EnumToEnum:
                builder.Append(sourceExpr).Append(" switch").NewLine();
                builder.Indent().Append("{").NewLine();
                var destMembersArr = mapping.DestEnumMembers;
                foreach (var memberName in mapping.SourceEnumMembers)
                {
                    var sourceType = !String.IsNullOrEmpty(mapping.SourceUnderlyingType) ? mapping.SourceUnderlyingType : mapping.SourceType;
                    if (destMembersArr.Contains(memberName, StringComparer.Ordinal))
                    {
                        builder.Indent().Append("    ")
                               .Append(sourceType).Append(".").Append(memberName)
                               .Append(" => ")
                               .Append(targetType).Append(".").Append(memberName)
                               .Append(",").NewLine();
                    }
                }
                builder.Indent().Append("    _ => default").NewLine();
                builder.Indent().Append("}");
                break;

            case EnumMappingKind.EnumToNumeric:
                builder.Append("(").Append(targetType).Append(")").Append(sourceExpr);
                break;

            case EnumMappingKind.NumericToEnum:
                builder.Append("(").Append(targetType).Append(")").Append(sourceExpr);
                break;

            case EnumMappingKind.EnumToString:
                // Member names are known at compile time; a switch returns cached string constants
                // without the boxing/name-lookup cost of Enum.ToString. Undefined values (e.g. flags
                // combinations) fall back to ToString to preserve semantics.
                if (mapping.SourceEnumMembers.Count > 0)
                {
                    var enumSourceType = !String.IsNullOrEmpty(mapping.SourceUnderlyingType) ? mapping.SourceUnderlyingType : mapping.SourceType;
                    builder.Append(sourceExpr).Append(" switch").NewLine();
                    builder.Indent().Append("{").NewLine();
                    foreach (var memberName in mapping.SourceEnumMembers)
                    {
                        builder.Indent().Append("    ")
                               .Append(enumSourceType).Append(".").Append(memberName)
                               .Append(" => \"").Append(memberName).Append("\",").NewLine();
                    }
                    builder.Indent().Append("    _ => ").Append(sourceExpr).Append(".ToString()").NewLine();
                    builder.Indent().Append("}");
                }
                else
                {
                    builder.Append(sourceExpr).Append(".ToString()");
                }
                break;

            case EnumMappingKind.StringToEnum:
                // A string switch (length/char dispatch) avoids the Enum.Parse machinery for exact
                // member names; anything else (numeric strings, whitespace, flags lists) falls back
                // to Enum.Parse to preserve semantics.
                if (mapping.DestEnumMembers.Count > 0)
                {
                    builder.Append(sourceExpr).Append(" switch").NewLine();
                    builder.Indent().Append("{").NewLine();
                    foreach (var memberName in mapping.DestEnumMembers)
                    {
                        builder.Indent().Append("    \"").Append(memberName).Append("\" => ")
                               .Append(targetType).Append(".").Append(memberName)
                               .Append(",").NewLine();
                    }
                    builder.Indent().Append("    _ => global::System.Enum.Parse<").Append(targetType).Append(">(").Append(sourceExpr).Append(")").NewLine();
                    builder.Indent().Append("}");
                }
                else
                {
                    builder.Append("global::System.Enum.Parse<").Append(targetType).Append(">(").Append(sourceExpr).Append(")");
                }
                break;
        }
    }
